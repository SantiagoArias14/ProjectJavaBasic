package EjemplosBucles;


public class EjercicioIf {
    public static void main(String[] args) {
        var NumeroIf = 0;

        if (NumeroIf == 0) {
            System.out.println("El numero que ingresaste es 0");
        } else if (NumeroIf > 0) {
            System.out.println("El numero que ingresate es positivo");
        } else if (NumeroIf < 0) {
            System.out.println("El numero que ingresaste es negativo");
    
        }
    }
}
